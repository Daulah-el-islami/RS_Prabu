    private void tampil_koderawatinap() {
        try {
            Class.forName("com.mysql.cj.jdbc.Driver").newInstance();
            Connection koneksi = DriverManager.getConnection("jdbc:mysql://localhost/db_rsprabu", "root", "");
            Statement stmt = koneksi.createStatement();
            ResultSet re = stmt.executeQuery("SELECT * FROM rawat_inap ORDER BY kd_inap ASC");

            while (re.next()) {
                C_kdrawatinap.addItem(re.getString("kd_inap"));
            }

            koneksi.close();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Terjadi Kesalahan Tampil Kode Rawat Inap: " + ex.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
