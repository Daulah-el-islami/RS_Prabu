    private void cetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cetakActionPerformed
        try {
            String NamaFile = "src/Laporan/report_rawatinap.jasper";
            Class.forName("com.mysql.cj.jdbc.Driver").newInstance();
            Connection koneksi = DriverManager.getConnection("jdbc:mysql://localhost/db_rsprabu", "root", "");
            HashMap hash = new HashMap();
            File file = new File(NamaFile);

            hash.put("kd_inap", C_kdrawatinap.getSelectedItem());
            JasperReport jasperReport = (JasperReport) JRLoader.loadObject(file.getPath());
            JasperPrint jasperPrint = JasperFillManager.fillReport(jasperReport, hash, koneksi);

            JasperViewer viewer = new JasperViewer(jasperPrint, false);
            viewer.setTitle("Laporan Rawat Inap");
            viewer.setVisible(true);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Terjadi Error Cetak " + e);
        }
    }//GEN-LAST:event_cetakActionPerformed

